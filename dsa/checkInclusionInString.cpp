#include <iostream>
#include <bits/stdc++.h>
using namespace std;

class Solution{
    public:
        bool isFreqSame(int freq1[], int freq2[]){
            for(int i= 0; i<26;i++){
                if(freq1[i] != freq2[i]){
                    return false;
                }
            }
            return true;    
        }


        bool checkInclusion(string s1, string s2){
            int freq[26] = {0};

            for(int i=0; i<s1.length(); i++){
                //a-> 0 , b->1 ayega
                freq[s1[i]-'a']++;
            }

            int windSize = s1.length();

            for(int i = 0; i<s2.length(); i++){
                int windIdx = 0, idx = i;
                int windFreq[26] = {0};

                while(windIdx < windSize && idx <s2.length()){
                    windFreq[s2[idx]-'a']++;
                    windIdx++; idx++;
                }

                if(isFreqSame(freq,windFreq)){
                    return true;
                }
            }
        }
};

int main(){
    Solution T;
    string s1 = "eidab  ooo";
    string s2 = "ab";
    cout<<T.checkInclusion(s1,s2)<<endl;
    if(T.checkInclusion(s1,s2)){
        cout<<"True"<<endl;
    }else{
        cout<<"False"<<endl;
    }


    
    return 0;
}   